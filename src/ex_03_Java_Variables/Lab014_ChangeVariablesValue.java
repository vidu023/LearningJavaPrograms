package ex_03_Java_Variables;

public class Lab014_ChangeVariablesValue {
    public static void main(String[] args) {
        int a = 257;
        System.out.println(a);
        a = 955; //changing the value of a
        //earlier value of a was 257 which will now change to 955
        //only value will get replaced
        System.out.println(a);
    }
}
