package ex_07_IncrementDecremet;

public class Lab070_PreIncrement_Ex2 {
    public static void main (String[] args) {
        // Pre Increment Example 2

        int age = 15;
        int new_age = ++age;

        System.out.println(new_age);
        System.out.println(age);

        //Exp & Result Table
        //Line No | age | Result new_age
        // 7     | 15 | NA
        // 8     | 16 | 16

    }
}
