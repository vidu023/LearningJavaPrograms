package ex_11_While;

public class Lab118_WhileLoop {
    public static void main(String[] args) {

        // While Loop is an entry controlled loop
        // we can use for / while loop anything
        int i = 0; // I
        while (i<10){ // C
            System.out.println("Enter the Program");
            System.out.println(i);
            i++; // U
        }
    }
}
