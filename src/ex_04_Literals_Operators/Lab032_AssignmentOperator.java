package ex_04_Literals_Operators;

public class Lab032_AssignmentOperator {
    public static void main(String[] args) {
        int salary = 55000;
        // int -> data type
        //salary -> variable name
        // 55000 -> value
        // = assignment operator

        // = is assigning value of 55000 to 'salary' variable name
        // from RHS to LHS -> assignment operator

        System.out.println(salary);    }
}
