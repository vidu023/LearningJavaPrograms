package ex_04_Literals_Operators;

public class Lab028_DifferentLiterals {
    public static void main(String[] args) {
        //Integer type of Literal
        int a = 4567;

        //byte type of literal
        byte b = 118;

        //boolean literal
        boolean is_married = true;

        //float type of literal
        float  f = 741.1547f;

        // long type of literal
        long d = 14987979787L;

        // double type of literal
        double m = 23.4812151544;

        //binary literal
        int binary = 0b1010; // we need to add 0b in front of a binary number

        //octal base add 0
        int octal = 0101;

        //hexadecimal add 16
        int hexa = 0Xface;

        //char literal
        char z = 'A';
        //char -> A to Z, a to z, !@#$%^&*_()[] all special characters
        // even space is also a char
        //char g ="A"; // double quote is a string & not a char
        char s = 'S';
        char pig = 'p';
        char ampersand = '&';
        char star = '*';
        char space = ' '; // space




    }
}
